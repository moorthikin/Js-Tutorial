//type conversion = change the data type of the variable value to another
//                  (strings, numbers, booleans)

let x = "Moorthi";
let y = "20";
let z = "TamilNadu";

x = String(x);
y = Number(y);
z = Boolean(z);

console.log(x, typeof x);
console.log(y, typeof y);
console.log(z, typeof z);
